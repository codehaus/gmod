package org.codehaus.groovy.science;

/**
 * <p>The Groovy operators that are overloaded for use with the
 * {@code SymbolicExpression} class.</p>
 */
public enum OverloadableOperators
{
	Plus,
	Minus,
	Multiply,
	Power,
	Div,
	Mod,
	Or,
	And,
	Xor,
	GetAt,
	LeftShift,
	RightShift,
	BitwiseNegate,
	Negative,
	Positive;
}